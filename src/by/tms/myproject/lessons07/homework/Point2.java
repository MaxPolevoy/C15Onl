package by.tms.myproject.lessons07.homework;

public class Point2 {

    private double x2;
    private double y2;

    public Point2(double x2, double y2) {
        this.x2 = x2;
        this.y2 = y2;
    }

    public void setX(double x2) {
        this.x2 = x2;
    }

    public void setY(double y2) {
        this.y2 = y2;
    }

    public double getX() {
        return x2;
    }

    public double getY() {
        return y2;
    }
}
